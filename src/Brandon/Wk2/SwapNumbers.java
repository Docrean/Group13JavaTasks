package Brandon.Wk2;

public class SwapNumbers {

    //Swap two variables' values without using a third variable

    public static void main(String[] args) {

        int num1 = 10;
        int num2 = 20;

        num1 = num2; //num1 now 20


        System.out.println(num1);
        System.out.println(num2);
    }
}
